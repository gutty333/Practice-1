// Basketball Player Height
// The star player is 73 inches tall
// Find the height of the player in feet

#include <iostream>
using namespace std;

int main()
{
	int inch = 73;
	double feet = inch / 12;

	cout << "Your height in inches is " << inch << endl;
	cout << "Your height in feet is " << feet << endl;

	return 0;
}